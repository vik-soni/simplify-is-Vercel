-- These are defined in the Agent 6 multiuser migration but several earlier
-- migrations reference them. Re-declaring with CREATE OR REPLACE removes the
-- run-order dependency flagged in CODE_REVIEW_FULL.md M4.

CREATE OR REPLACE FUNCTION public.is_org_member(target_org UUID)
RETURNS BOOLEAN
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT EXISTS (
    SELECT 1
    FROM public.users
    WHERE id = auth.uid()
      AND org_id = target_org
      AND status = 'active'
  )
  OR EXISTS (
    SELECT 1
    FROM public.organizations
    WHERE id = target_org
      AND owner_user_id = auth.uid()
  );
$$;

CREATE OR REPLACE FUNCTION public.is_org_admin(target_org UUID)
RETURNS BOOLEAN
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT EXISTS (
    SELECT 1
    FROM public.users
    WHERE id = auth.uid()
      AND org_id = target_org
      AND role = 'admin'
      AND status = 'active'
  )
  OR EXISTS (
    SELECT 1
    FROM public.organizations
    WHERE id = target_org
      AND owner_user_id = auth.uid()
  );
$$;

