DROP POLICY IF EXISTS "Authenticated read risk mappings" ON public.risk_control_mappings;
CREATE POLICY "Authenticated read risk mappings"
  ON public.risk_control_mappings
  FOR SELECT
  USING (auth.role() = 'authenticated');

-- COMMENT clarifies intent — this is reference data, public to all signed-in
-- users.
COMMENT ON TABLE public.risk_control_mappings IS
  'Reference data: risk-to-control mappings per framework. Read-only to all authenticated users; writes via service role only.';

-- Stop duplicates on the natural key.
DO $$
BEGIN
  IF NOT EXISTS (
    SELECT 1
    FROM pg_constraint
    WHERE conname = 'risk_control_mappings_natural_uq'
  ) THEN
    ALTER TABLE public.risk_control_mappings
      ADD CONSTRAINT risk_control_mappings_natural_uq
      UNIQUE (risk_id, control_id, framework_id);
  END IF;
END $$;

-- ─────────────────────────────────────────────
-- C5  audit_log — preserve trail when org is deleted
-- ─────────────────────────────────────────────
-- Drop the existing FK and recreate with ON DELETE SET NULL so audit records
-- outlive the organisations they describe. Constraint name is generated by
-- PostgreSQL; query pg_constraint to find it dynamically.

DO $$
DECLARE
  conname_to_drop TEXT;
BEGIN
  SELECT conname INTO conname_to_drop
  FROM pg_constraint
  WHERE conrelid = 'public.audit_log'::regclass
    AND contype = 'f'
    AND pg_get_constraintdef(oid) ILIKE '%REFERENCES%organizations%';

  IF conname_to_drop IS NOT NULL THEN
    EXECUTE format(
      'ALTER TABLE public.audit_log DROP CONSTRAINT IF EXISTS %I',
      conname_to_drop
    );
  END IF;
END $$;

ALTER TABLE public.audit_log
  ADD CONSTRAINT audit_log_organization_id_fkey
  FOREIGN KEY (organization_id)
  REFERENCES public.organizations(id)
  ON DELETE SET NULL;

-- Same for session_metadata_log — same compliance reasoning.
DO $$
DECLARE
  conname_to_drop TEXT;
BEGIN
  SELECT conname INTO conname_to_drop
  FROM pg_constraint
  WHERE conrelid = 'public.session_metadata_log'::regclass
    AND contype = 'f'
    AND pg_get_constraintdef(oid) ILIKE '%REFERENCES%organizations%';

