-- ─────────────────────────────────────────────
-- H5  audit_log payload sanitisation
-- ─────────────────────────────────────────────
-- Strip well-known secret column names from the JSONB payload before storage,
-- so a row containing a password/token never lands in audit_log.changed_data.

CREATE OR REPLACE FUNCTION public.sanitize_audit_payload(payload JSONB)
RETURNS JSONB
LANGUAGE sql
IMMUTABLE
AS $$
  SELECT payload
    - 'password'
    - 'password_hash'
    - 'access_token'
    - 'refresh_token'
    - 'api_key'
    - 'anthropic_api_key'
    - 'orchestration_secret'
    - 'service_key'
    - 'stripe_secret_key'
    - 'stripe_webhook_secret'
    - 'resend_api_key'
    - 'session_token'
    - 'authorization'
    - 'cookie'
$$;

-- The existing write_audit_log() trigger (20250320000001) uses to_jsonb(NEW)
-- directly. Wrap that output via the sanitiser. We use CREATE OR REPLACE so
-- subsequent runs of the original migration win't re-introduce the unsafe
-- version (Supabase typically runs migrations in order and only once, so this
-- is a belt-and-braces).

CREATE OR REPLACE FUNCTION public.write_audit_log()
RETURNS TRIGGER
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
DECLARE
  payload JSONB;
BEGIN
  IF TG_OP = 'DELETE' THEN
    payload := public.sanitize_audit_payload(to_jsonb(OLD));
  ELSE
    payload := public.sanitize_audit_payload(to_jsonb(NEW));
  END IF;

  INSERT INTO public.audit_log (
    table_name,
    record_id,
    action,
    user_id,
    organization_id,
    changed_data
  )
  VALUES (
    TG_TABLE_NAME,
    COALESCE((NEW.id)::TEXT, (OLD.id)::TEXT),
    TG_OP,
    auth.uid(),
    CASE
      WHEN TG_TABLE_NAME = 'organizations' THEN COALESCE(NEW.id, OLD.id)
      ELSE COALESCE(
        (payload->>'organization_id')::UUID,
        (payload->>'org_id')::UUID
      )
    END,
    payload
  );

  RETURN COALESCE(NEW, OLD);
END;
$$;

-- ─────────────────────────────────────────────
-- H4  org_audit_trail — INSERT only by org admins, only as self
-- ─────────────────────────────────────────────

DROP POLICY IF EXISTS "Members insert org audit trail" ON public.org_audit_trail;
DROP POLICY IF EXISTS "Org members insert" ON public.org_audit_trail;
DROP POLICY IF EXISTS "Admins insert org audit trail" ON public.org_audit_trail;

CREATE POLICY "Admins insert org audit trail"
  ON public.org_audit_trail
  FOR INSERT
  WITH CHECK (
    public.is_org_admin(org_id)
    AND performed_by = auth.uid()
  );

-- ─────────────────────────────────────────────
-- M10  cypher_assessment_transcripts — require session ownership on INSERT
-- ─────────────────────────────────────────────

DROP POLICY IF EXISTS "Cypher transcripts user-only insert" ON public.cypher_assessment_transcripts;

CREATE POLICY "Cypher transcripts user-only insert"
  ON public.cypher_assessment_transcripts
  FOR INSERT
  WITH CHECK (
    user_id = auth.uid()
    AND EXISTS (
      SELECT 1
      FROM public.assessment_sessions s
      WHERE s.id = session_id
        AND s.user_id = auth.uid()
    )
  );

